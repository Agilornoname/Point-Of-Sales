    private class SalesDetailTableModel extends AbstractTableModel {

        private List<SalesDetail> salesDetails;

        SalesDetailTableModel(List<SalesDetail> salesDetails) {
            this.salesDetails = salesDetails;
        }

        public void Remove(int row) {
            salesDetails.remove(row);
        }

        public int getRowCount() {
            return salesDetails.size();
        }

        public int getColumnCount() {
            return 5;
        }

        public Object getValueAt(int rowIndex, int columnIndex) {
            SalesDetail s = salesDetails.get(rowIndex);
            switch (columnIndex) {
                case 0:
                    return s.getProduct().getId();
                case 1:
                    return s.getProduct().getName();
                case 2:
                    return s.getPrice();
                case 3:
                    return s.getQuantity();
                case 4:
                    return s.getSubtotal();
                default:
                    return "";
            }
        }

        @Override
        public Class<?> getColumnClass(int columnIndex) {
            if (columnIndex == 2 || columnIndex == 4) {
                return BigDecimal.class;
            } else if (columnIndex == 3) {
                return Integer.class;
            }
            return String.class;
        }

        @Override
        public boolean isCellEditable(int rowIndex, int columnIndex) {
            if (columnIndex == 3) {
                return true;
            }
            return false;
        }

        @Override
        public void setValueAt(Object aValue, int rowIndex, int columnIndex) {
            SalesDetail s = salesDetails.get(rowIndex);
            if (columnIndex == 3) {
                s.setQuantity((Integer) aValue);
                s.setSubtotal(s.getPrice().multiply(new BigDecimal(s.getQuantity())));
                refreshTotalLabel();
            }
        }
    }

