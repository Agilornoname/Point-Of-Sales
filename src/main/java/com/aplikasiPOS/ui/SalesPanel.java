    private void txtBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtBarangActionPerformed
        String productId = txtBarang.getText();
        try {
//cari apakah barang sudah ada di dalam tblSalesDetail
            Long pId = Long.valueOf(productId);
            boolean isProductInSalesDetails = false;
            for (SalesDetail salesDetail : listSalesDetails) {
                if (salesDetail.getId() != null && salesDetail.getProduct().getId().equals(pId)) {
                    salesDetail.setQuantity(salesDetail.getQuantity() + 1);
                    salesDetail.setSubtotal(salesDetail.getPrice().multiply(new BigDecimal(salesDetail.getQuantity())));
                    isProductInSalesDetails = true;
                    break;
                }
            }
            if (isProductInSalesDetails) {
                refreshTable();
                refreshTotalLabel();
            } else {
                Product p = Main.getProductService().getProduct(pId);
                if (p != null) {
                    addSalesDetail(p);
                }
            }
            txtTransaksi.setText("");
        } catch (NumberFormatException numberFormatException) {
            JOptionPane.showMessageDialog(this, "Id barang harus berupa angka!",
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_txtBarangActionPerformed

