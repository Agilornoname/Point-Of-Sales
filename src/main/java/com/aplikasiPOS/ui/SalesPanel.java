    private void clearForm() {
        txtBarang.setText("");
        lblHarga.setText("Rp. 0");
        jDateChooser1.setDate(new Date());
        listSalesDetails = new ArrayList<SalesDetail>();
        sales = null;
        tblSales.setModel(new SalesDetailTableModel(listSalesDetails));
    }

    private void enableForm(boolean status) {
        txtTransaksi.setEnabled(status);
        btnLookUp.setEnabled(status);
        tblSales.setEnabled(status);
    }

    private boolean validateForm() {
        if (listSalesDetails == null || listSalesDetails.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Transaksi tidak boleh kosong!", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }

    private void loadFormToModel() {
        sales.setSalesDetails(listSalesDetails);
        sales.setSalesDate(new Date());
        BigDecimal total = BigDecimal.ZERO;
        for (SalesDetail salesDetail : listSalesDetails) {
            total = total.add(salesDetail.getSubtotal());
            salesDetail.setSales(sales);
        }
        sales.setTotalSales(total);
    }

    private void loadModelToForm() {
        listSalesDetails = sales.getSalesDetails();
        tblSales.setModel(new SalesDetailTableModel(listSalesDetails));
        lblHarga.setText(TextComponentUtils.formatNumber(sales.getTotalSales()));
    }

    private void refreshTable() {
        tblSales.setModel(new SalesDetailTableModel(listSalesDetails));
    }

    private void refreshTotalLabel() {
        if (listSalesDetails != null && !listSalesDetails.isEmpty()) {
            BigDecimal total = BigDecimal.ZERO;
            for (SalesDetail salesDetail : listSalesDetails) {
                total = total.add(salesDetail.getSubtotal());
            }
            lblHarga.setText("Rp. " + TextComponentUtils.formatNumber(total));
        }
    }

