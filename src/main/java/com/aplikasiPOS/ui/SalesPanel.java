    private void btnLookUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLookUpActionPerformed
        Product p = new ProductLookUpDialog().getProduct();
        if (p != null) {
            boolean isProductInSalesDetails = false;
            for (SalesDetail salesDetail : listSalesDetails) {
                if (salesDetail.getId() != null && salesDetail.getProduct().getId().equals(p.getId())) {
                    salesDetail.setQuantity(salesDetail.getQuantity() + 1);
                    salesDetail.setSubtotal(salesDetail.getPrice().multiply(new BigDecimal(salesDetail.getQuantity())));
                    isProductInSalesDetails = true;
                    break;
                }
            }
            if (isProductInSalesDetails) {
                refreshTable();
                refreshTotalLabel();
            } else {
                addSalesDetail(p);
            }
        }
        jButton1.setEnabled(true);
    }//GEN-LAST:event_btnLookUpActionPerformed

